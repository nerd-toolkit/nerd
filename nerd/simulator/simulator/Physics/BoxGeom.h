#ifndef BoxGeom_H_
#define BoxGeom_H_

#include "TriangleGeom.h"
#include "Value/DoubleValue.h"


/**
* BoxGeom. Special TriangleGeom for representing a Box with variable widht, height and depth.
*/
namespace nerd {
class BoxBody;

class BoxGeom : public TriangleGeom {

	public:
		BoxGeom(SimObject *sim, double width, double height, double depth);
		BoxGeom(const BoxGeom &geom);
		virtual ~BoxGeom();
		
		virtual SimGeom* createCopy() const;

		void setSize(double width, double height, double depth);
		
		double getDepth() const;
		double getHeight() const;
		double getWidth() const;

private:
	double mWidth;
	double mHeight;
	double mDepth;
};
}
#endif
