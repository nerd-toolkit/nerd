function reset() {
	if(firstReset) {
		exec();
	}
	firstReset = false;
}

function exec() {
	print("HO");
	for(var i = 0; i < spinalCordAdresses.length; i++) {
		var neuronNameIdPair = spinalCordAdresses[i].split(",");
		if(neuronNameIdPair.length != 2) {
			print("MSeriesSpinalCordTagger: Could not parse line: " + spinalCordAdresses[i]);
			continue;
		}
		var neuron = net.getNeuron(neuronNameIdPair[0]);
		if(neuron != 0) {
			net.setProperty(neuron, "SPINAL_CORD_ADDRESS", neuronNameIdPair[1]);
		}
	}
	print("HOHO");
}

